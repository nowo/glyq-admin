<template>
    <div v-show="!isTagsViewCurrentFull" class="h100%">
        <el-aside class="layout-aside" :class="setCollapseStyle">
            <Logo v-if="setShowLogo" />
            <ElScrollbar ref="layoutAsideScrollbarRef" class="flex-auto" @mouseenter="onAsideEnterLeave(true)"
                @mouseleave="onAsideEnterLeave(false)">
                <Vertical :menu-list="state.menuList" />
            </ElScrollbar>
        </el-aside>
    </div>
</template>

<script lang="ts" setup>
import { computed, onBeforeMount, reactive, ref, watch } from 'vue'
import { storeToRefs } from 'pinia'
import type { RouteRecordRaw } from 'vue-router'
import { useEventBus } from '@vueuse/core'
import { ElScrollbar } from 'element-plus'
import { useRoutesList } from '@/stores/routesList'
import { useThemeConfig } from '@/stores/themeConfig'
import { useTagsViewRoutes } from '@/stores/tagsViewRoutes'
import Logo from '@/layout/logo/index.vue'
import Vertical from '@/layout/navMenu/vertical.vue'

const layoutAsideScrollbarRef = ref<InstanceType<typeof ElScrollbar>>()

const stores = useRoutesList()
const storesThemeConfig = useThemeConfig()
const storesTagsViewRoutes = useTagsViewRoutes()
const { routesList } = storeToRefs(stores)

const { themeConfig } = storeToRefs(storesThemeConfig)
const { isTagsViewCurrentFull } = storeToRefs(storesTagsViewRoutes)

const setMenuBus = useEventBus('setMenuList')
const layoutResizeBus = useEventBus<{ clientWidth: number; layout: MainLayoutType }>('layoutMobileResize')
const setFilterRouteBus = useEventBus('getBreadcrumbIndexSetFilterRoutes')
const setSendColumnsChildBus = useEventBus<any>('setSendColumnsChild')
const setSendClassicChildBus = useEventBus<any>('setSendClassicChild')

const state = reactive({
    menuList: [] as RouteRecordRaw[],
    numberList: [] as MenuApi_MenuNumberResponse[],
    clientWidth: 0,
})

// 设置菜单展开/收起时的宽度
const setCollapseStyle = computed(() => {
    const { layout, isCollapse, menuBar } = themeConfig.value
    const asideBrTheme = ['#FFFFFF', '#FFF', '#fff', '#ffffff']
    const asideBrColor = asideBrTheme.includes(menuBar) ? 'layout-el-aside-br-color' : ''
    // 判断是否是手机端
    if (state.clientWidth <= 1000) {
        if (isCollapse) {
            document.body.setAttribute('class', 'el-popup-parent--hidden')
            const asideEle = document.querySelector('.layout-container') as HTMLElement
            const modeDiv = document.createElement('div')
            modeDiv.setAttribute('class', 'layout-aside-mobile-mode')
            asideEle.appendChild(modeDiv)
            modeDiv.addEventListener('click', closeLayoutAsideMobileMode)
            return [asideBrColor, 'layout-aside-mobile', 'layout-aside-mobile-open']
        } else {
            // 关闭弹窗
            closeLayoutAsideMobileMode()
            return [asideBrColor, 'layout-aside-mobile', 'layout-aside-mobile-close']
        }
    } else {
        if (layout === 'columns') {
            // 分栏布局，菜单收起时宽度给 1px
            if (isCollapse) return [asideBrColor, 'layout-aside-pc-1']
            else return [asideBrColor, 'layout-aside-pc-220']
        } else {
            // 其它布局给 64px
            if (isCollapse) return [asideBrColor, 'layout-aside-pc-64']
            else return [asideBrColor, 'layout-aside-pc-220']
        }
    }
})
// 关闭移动端蒙版
const closeLayoutAsideMobileMode = () => {
    const el = document.querySelector('.layout-aside-mobile-mode')
    el?.setAttribute('style', 'animation: error-img-two 0.3s')
    setTimeout(() => {
        el?.parentNode?.removeChild(el)
    }, 300)
    const clientWidth = document.body.clientWidth
    if (clientWidth < 1000) themeConfig.value.isCollapse = false
    document.body.setAttribute('class', '')
}
// 设置显示/隐藏 logo
const setShowLogo = computed(() => {
    const { layout, isShowLogo } = themeConfig.value
    return (isShowLogo && layout === 'defaults') || (isShowLogo && layout === 'columns')
})
// 设置/过滤路由（非静态路由/是否显示在菜单中）
const setFilterRoutes = () => {
    if (themeConfig.value.layout === 'columns') return false
    state.menuList = filterRoutesFun(routesList.value)
}
// 路由过滤递归函数
const filterRoutesFun = (arr: Array<RouteRecordRaw>) => {
    return arr
        .filter((item: any) => !item.meta.isHide)
        .map((item: any) => {
            item = Object.assign({}, item)
            if (item.children) item.children = filterRoutesFun(item.children)
            return item
        })
}
// 设置菜单导航是否固定（移动端）
const initMenuFixed = (clientWidth: number) => {
    state.clientWidth = clientWidth
}
// 鼠标移入、移出
const onAsideEnterLeave = (bool: boolean) => {
    const { layout } = themeConfig.value
    if (layout !== 'columns') return false
    if (!bool) setMenuBus.emit()
    stores.setColumnsMenuHover(bool)
}

// 监听 themeConfig 配置文件的变化，更新菜单 el-scrollbar 的高度
watch(themeConfig.value, (val) => {
    if (val.isShowLogoChange !== val.isShowLogo) layoutAsideScrollbarRef.value?.update()

    const { layout, isClassicSplitMenu } = val
    if (layout === 'classic' && isClassicSplitMenu) return false
    setFilterRoutes()
})

// // 监听vuex值的变化，动态赋值给菜单中
// watch(pinia.state, (val) => {
//     console.log('val :>> ', val);
//     let { layout, isClassicSplitMenu } = val.themeConfig.themeConfig;
//     if (layout === 'classic' && isClassicSplitMenu) return false;
//     setFilterRoutes();
// }, {
//     deep: true,
// });

// 页面加载前
onBeforeMount(() => {
    initMenuFixed(document.body.clientWidth)
    setFilterRoutes()
    // 此界面不需要取消监听(proxy.mittBus.off('setSendColumnsChild'))
    // 因为切换布局时有的监听需要使用，取消了监听，某些操作将不生效
    setSendColumnsChildBus.on((res) => {
        state.menuList = res.children
    })
    setSendClassicChildBus.on((res) => {
        const { layout, isClassicSplitMenu } = themeConfig.value
        if (layout === 'classic' && isClassicSplitMenu) {
            state.menuList = []
            state.menuList = res.children
        }
    })
    setFilterRouteBus.on(() => {
        setFilterRoutes()
    })
    layoutResizeBus.on((res) => {
        initMenuFixed(res.clientWidth)
        closeLayoutAsideMobileMode()
    })
})
</script>
